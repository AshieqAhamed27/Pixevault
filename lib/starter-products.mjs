export function getProductImage(slug) {
  return `/product-images/${slug}.png`;
}

export function getSeedProducts() {
  return starterProducts.map((product) => ({
    ...product,
    image: product.image || getProductImage(product.slug),
  }));
}

export function getPublicStarterProducts(category = 'all') {
  return getSeedProducts()
    .filter((product) => product.active && (category === 'all' || product.category === category))
    .map((product) => ({
      _id: product.slug,
      ...product,
      createdAt: '2026-05-18T00:00:00.000Z',
      updatedAt: '2026-05-18T00:00:00.000Z',
    }));
}
